public class Volume {
    public static void main(String[] args) {
        float volume;
        float radius = 10.0f, height = 10.0f;
        volume = 3.1416f * radius * radius * height;
        System.out.println("Обьем цилиндра с площадью основания " + 3.1416f * radius * radius + " и высотой " + height + " равен: " + volume);
    }

}
